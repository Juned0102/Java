package MethodsExample;

public class Calculator {
	//addition 
	//substraction/differance
	//multiplication
	//division
	
	public int getsum(int num1,int num2) {
		return num1+num2;
		
	}
	public int getsubstraction(int num1,int num2) {
		return num1-num2;
	}
	
	public int getmultiplication(int num1,int num2) {
		return num1*num2;
	}
	
	public int getdivision(int num1,int num2) {
		return num1/num2;
	}

	public int getdsumthree(int num1,int num2,int num3) {
		return num1+num2+num3;
	}
	
}
